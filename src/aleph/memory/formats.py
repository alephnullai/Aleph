DECISIONS_END = "[/DECISIONS]"
CONCLUSIONS_TAG = "[CONCLUSIONS]"
CONCLUSIONS_END = "[/CONCLUSIONS]"
OPEN_QUESTIONS_TAG = "[OPEN_QUESTIONS]"
OPEN_QUESTIONS_END = "[/OPEN_QUESTIONS]"
CODE_CHANGES_TAG = "[CODE_CHANGES]"
CODE_CHANGES_END = "[/CODE_CHANGES]"
ERRORS_TAG = "[ERRORS_ENCOUNTERED]"
ERRORS_END = "[/ERRORS_ENCOUNTERED]"
