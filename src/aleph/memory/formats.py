CONTEXT_END = "[/CONTEXT]"
DICT_TAG = "[DICT]"
DICT_END = "[/DICT]"
STATS_TAG = "[STATS]"
STATS_END = "[/STATS]"

# Resume briefing tags (project.aleph.resume)
RESUME_HEADER = f"[ALEPH:RESUME:{FORMAT_VERSION}]"
